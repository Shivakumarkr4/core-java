  {
    return langauge;
  }
 
  public void setLanguage(String langauge)
  {
   this.langauge= langauge;
  }
  
  public String getType()
  {
    return type;
  }
 
 public void setType(String type)
 {
   this.type = type;
 }
 
 
 
 
 
 
 
 
 
 

 
 
 
 
 
 
